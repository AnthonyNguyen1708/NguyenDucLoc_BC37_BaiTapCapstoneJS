function Product(
  id,
  name,
  price,
  screen,
  backCamera,
  frontCamera,
  img,
  des,
  type
) {
  (this.id = id),
    (this.name = name),
    (this.price = price),
    (this.screen = screen),
    (this.backCamera = backCamera),
    (this.frontCamera = frontCamera),
    (this.img = img),
    (this.des = des),
    (this.type = type);
}
